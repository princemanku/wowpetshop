                                     <p align="justify">Boxers are a bright, energetic and playful breed and tend to be very good with children. They are patient and spirited with children but also protective, making them a popular choice for families. They are active, strong dogs and require adequate exercise to prevent boredom-associated behaviors such as chewing, digging, or licking. Boxers have earned a slight reputation of being "headstrong," which can be related to inappropriate obedience training. Owing to their intelligence and working breed characteristics, training based on corrections often has limited usefulness. Boxers, like other animals, typically respond better to positive reinforcement techniques such as clicker training, an approach based on operant conditioning and behaviorism, which offers the dog an opportunity to think independently and to problem-solve. Stanley Coren's survey of obedience trainers, summarized in his book The Intelligence of Dogs, ranked Boxers at #48 &ndash; average working/obedience intelligence. Many who have worked with Boxers disagree quite strongly with Coren's survey results, and maintain that a skilled trainer who uses reward-based methods will find Boxers have far above-average intelligence and working ability.<br /><br />The Boxer by nature is not an aggressive or vicious breed. It is an instinctive guardian and can become very attached to its family. Like all dogs, it requires proper socialization. Boxers are generally patient with smaller dogs and puppies, but difficulties with larger adult dogs, especially those of the same sex, may occur. Boxers are generally more comfortable with companionship, in either human or canine form.</p>                                </div>
