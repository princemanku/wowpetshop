                                     <p align="justify">The beagle has an even temper and gentle disposition. Described in several breed standards as "merry", they are amiable and typically neither aggressive nor timid, although this depends on the individual. They enjoy company, and although they may initially be standoffish with strangers, they are easily won over. They make poor guard dogs for this reason, although their tendency to bark or howl when confronted with the unfamiliar makes them good watch dogs. In a 1985 study conducted by Ben and Lynette Hart, the beagle was given the highest excitability rating, along with the Yorkshire Terrier, Cairn Terrier, Miniature Schnauzer, West Highland White Terrier, and Fox Terrier.<br /><br />Beagles are intelligent but, as a result of being bred for the long chase, are single-minded and determined, which can make them hard to train. They can be difficult to recall once they have picked up a scent, and are easily distracted by smells around them. They do not generally feature in obedience trials; while they are alert, respond well to food-reward training, and are eager to please, they are easily bored or distracted. They are ranked 72nd in Stanley Coren's The Intelligence of Dogs, as Coren places them among the group with the lowest degree of working/obedience intelligence. Coren's scale, however, does not assess understanding, independence, or creativity.<br /><br />Beagles are excellent with children and this is one of the reasons they have become popular family pets, but they are pack animals, and are prone to separation anxiety, a condition which causes them to destroy things when left unattended. Not all beagles will howl, but most will bark when confronted with strange situations, and some will bay (also referred to as "speaking", "giving tongue", or "opening") when they catch the scent of potential quarry.[45] They also generally get along well with other dogs. They are not too demanding with regard to exercise; 